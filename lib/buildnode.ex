defmodule Tapestry.BuildNode do 
  
  def build_topology(numNodes, nodelist) when numNodes == 0 do
    nodelist
  end
  def build_topology(numNodes, nodelist) when numNodes > 0 do
    nodename = "node"<>Integer.to_string(numNodes) |> String.to_atom()
    id = Base.encode16(:crypto.hash(:sha, Integer.to_string(numNodes)))
    #IO.puts "node #{inspect nodename}   id #{id}"
    build_node(nodename, id)
    find_neighbor(id, nodename, nodelist) 
    nodelist = nodelist ++ [nodename]
    #IO.puts "nodelist #{inspect nodelist}"
    build_topology(numNodes-1, nodelist)
  end

  ########### start up all nodes in a required topology ############
  def build_node(nodename, id) do
